public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello 'Shreyash'..");     // Hello 'Shreyash'..
        System.out.println("Hello \"Shreyash\"..");   // Hello "Shreyash"..
        System.out.println("Hello \\Shreyash..");     // Hello \Shreyash..
        System.out.println("This is \t Space..");     // This is 	 Space..
        System.out.println("This is \nnew line");
        // This is
        // new line
    }
}
