public class DataType {
    public static void main(String[] args) {
        byte age = 25;                 // Small integer
        short salary = 15000;          // Medium integer
        int population = 2000000000;   // Large integer
        long earthAge = 4543000000L;   // Very large integer (use 'L')

        float pi = 3.14f;              // Decimal (use 'f')
        double bigPi = 3.1415926535;   // High-precision decimal

        char grade = 'A';              // Single character '' quote useed
        boolean isJavaFun = true;      // True/False
        String wishes = "Hello";

        System.out.println(age);       // 25
        System.out.println(salary);    // 15000
        System.out.println(population); // 2000000000
        System.out.println(earthAge);  // 4543000000
        System.out.println(pi);        // 3.14
        System.out.println(bigPi);     // 3.1415926535
        System.out.println(grade);     // A
        System.out.println(isJavaFun); // true
        System.out.println(wishes);    // Hello

    }
}
